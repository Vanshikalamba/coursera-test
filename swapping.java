import java.util.*;
class swapping
{
public static void main(String args[ ])
{
int a,b,c;
Scanner sc = new Scanner(System.in);
System.out.println("enter a and b");
a=sc.nextInt();
b=sc.nextInt();
c=a;
a=b;
b=c;
System.out.println(" after swapping :");
System.out.println("a =" +a);
System.out.println("b="+b);
}
}

